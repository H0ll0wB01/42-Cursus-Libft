#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*p;
	int		i;

	p = (char *)s;
	i = 0;
	while (p[i])
	{
		if (p[i] == c)
		{
			return (&p[i]);
		}
		i++;
	}
	return (NULL);
}
