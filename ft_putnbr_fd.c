#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	temp_n;

	if (n < 0)
	{
		write (fd, "-", 1);
		if (n == -2147483648)
		{
			write(fd, "2147483648", 10);
			return ;
		}
		n = -n;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	temp_n = (n % 10) + 48;
	write(fd, &temp_n, 1);
}
